package com.aryanonline.Model;

public class Pro_Colours {
    String Color;
    String Name;
    public Pro_Colours(String color, String name) {
        this.Color = color;
        this.Name = name;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }




}
